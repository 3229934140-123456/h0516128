import type { Application, CommunicationRecord, StageRecord } from '@/types';

const now = new Date();
const daysAgo = (days: number) => new Date(now.getTime() - days * 24 * 60 * 60 * 1000).toISOString();

const createStageHistory = (stage: string, days: number): StageRecord[] => {
  const history: StageRecord[] = [];
  const stages = ['initial', 'review', 'contract', 'preparation', 'completed'];
  const currentIndex = stages.indexOf(stage);
  
  for (let i = 0; i <= currentIndex; i++) {
    const isCurrent = i === currentIndex;
    history.push({
      stage: stages[i] as any,
      status: isCurrent ? 'processing' : 'completed',
      startedAt: daysAgo(days + (currentIndex - i) * 5),
      completedAt: isCurrent ? undefined : daysAgo(days + (currentIndex - i) * 5 - 3),
      note: isCurrent ? '正在跟进中' : '阶段顺利完成',
      operatorId: 'agent-001'
    });
  }
  return history;
};

const createCommunications = (count: number): CommunicationRecord[] => {
  const types = ['phone', 'meeting', 'email', 'other'];
  const contents = [
    '初次电话沟通，了解客户基本情况，对加盟意向进行初步确认',
    '客户来访总部，详细介绍品牌文化和加盟政策，参观样板店',
    '发送加盟资料包和费用明细，解答客户疑问',
    '确认投资预算和选址意向，进入资质审核阶段',
    '审核通过，准备加盟协议文件'
  ];
  
  return Array.from({ length: count }, (_, i) => ({
    id: `comm-${Date.now()}-${i}`,
    applicationId: '',
    agentId: 'agent-001',
    agentName: '李经理',
    content: contents[i % contents.length],
    type: types[i % types.length] as any,
    nextFollowUp: i === count - 1 ? '下周再次电话跟进' : undefined,
    createdAt: daysAgo(count - i)
  }));
};

